

CREATE TABLE MEMBER(
    USERID VARCHAR2(20) PRIMARY KEY,
    USERPWD VARCHAR2(50) NOT NULL,
    MEM_NAME VARCHAR2(20) NOT NULL,
    GENDER VARCHAR2(10) CHECK(GENDER IN('M','F')),
    EMAIL VARCHAR2(100) NOT NULL,
    PHONE VARCHAR2(50) NOT NULL,
    ADDRESSS VARCHAR2(200) NOT NULL,
    CHILD_NAME VARCHAR2(20) NOT NULL,
    CHILD_BIRTH DATE NOT NULL,
    CHILD_GENDER VARCHAR2(10) CHECK(CHILD_GENDER IN('M','F')),
    CHILD_CLASS VARCHAR2(20) NOT NULL
);

CREATE TABLE CATEGORY (
       BOARDTYPE NUMBER PRIMARY KEY,
       BOARDNAME VARCHAR2(300) NOT NULL
