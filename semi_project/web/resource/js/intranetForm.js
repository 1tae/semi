function formCategory(){
    var form = document.getElementById("formCategory");
    var content = document.getElementById("formContent");

    if(form.value = "지출결의서") {
        content.innerHTML  = '지출결의서 입니다';
    } else if(form.value = "휴가신청서") {
        content.innerHTML = "휴가신청서"
    }






}